    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String name = inputName.getText();

        try {
            obatModel.update(Integer.parseInt(selectedObatId), name);
            System.out.println("obat updated : " + selectedObatId);

            resetForm();

            JOptionPane.showMessageDialog(null, "obat telah diupdate");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        initTable();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Hapus Pasien?") == 0) {
            try {
                obatModel.delete(Integer.parseInt(selectedObatId));
                resetForm();
            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        initTable();
    }//GEN-LAST:event_btnHapusActionPerformed

