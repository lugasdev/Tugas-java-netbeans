    private void tableObatMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableObatMouseClicked
        System.out.println("registrationTableMouseClicked");
        int row = tableObat.rowAtPoint(evt.getPoint());
        int col = tableObat.columnAtPoint(evt.getPoint());

        System.out.println(row);
        System.out.println(col);

        selectedObatId = tableObat.getValueAt(row, 0).toString();
        System.out.println(selectedObatId);

        try {
            ResultSet obat = obatModel.get(Integer.parseInt(selectedObatId));
            while (obat.next()) {
                String name = obat.getString("name");

                inputName.setText(name);

                btnEdit.setEnabled(true);
                btnHapus.setEnabled(true);
            }
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_tableObatMouseClicked

