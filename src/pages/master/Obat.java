    public void initTable() {
        System.out.println("initTable");
        DefaultTableModel RecordTable = (DefaultTableModel) tableObat.getModel();
        RecordTable.setRowCount(0);

        String[] columns = {"id", "Nama Obat"};
        RecordTable.setColumnIdentifiers(columns);

        try {
            ResultSet obat = obatModel.get();

            while (obat.next()) {
                String id = obat.getString("id");
                String name = obat.getString("name");

                String[] row = {id, name};
                RecordTable.addRow(row);
            }

            tableObat.getColumnModel().getColumn(0).setMinWidth(30);
            tableObat.getColumnModel().getColumn(0).setMaxWidth(30);
            tableObat.getColumnModel().getColumn(0).setWidth(30);
        } catch (Exception e) {
            System.out.println("obat initTable");
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

